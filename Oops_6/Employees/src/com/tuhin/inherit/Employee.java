package com.tuhin.inherit;

public class Employee {
    String name;
    int age;
    char gender;
    int year_of_join;
    String mail_id;

    public Employee(String name, int age, char gender, int year_of_join, String mail_id) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.year_of_join = year_of_join;
        this.mail_id = mail_id;
    }
}
